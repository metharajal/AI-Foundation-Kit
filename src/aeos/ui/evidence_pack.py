def generate_evidence_pack(
    memory_dir: Path,
    project_name: str,
    output_dir: Path,
    overwrite: bool = False,
) -> EvidencePackResult:
    """Generate all evidence pack files into output_dir.

    Raises FileNotFoundError if memory_dir doesn't exist.
    Raises ValueError if no records found or output_dir is non-empty
    without --overwrite.
    """
    if output_dir.exists() and any(output_dir.iterdir()) and not overwrite:
        raise ValueError(
            f"'{output_dir}' already exists and is not empty."
            " Use --overwrite to replace it."
        )

    dash_data: DashboardData = load_dashboard_data(memory_dir, project_name)
    ws_data: WorkspaceData = load_workspace_data(memory_dir, project_name)

    output_dir.mkdir(parents=True, exist_ok=True)

    written: list[Path] = []

    def _write(name: str, content: str) -> Path:
        p = output_dir / name
        p.write_text(content, encoding="utf-8")
        written.append(p)
        return p

    _write("dashboard.html", render_dashboard(dash_data))
    _write("project-workspace.html", render_workspace(ws_data))
    _write("recovery-summary.md", render_recovery_summary(ws_data))
    _write("risk-register.md", render_risk_register(ws_data))
    _write("human-gates.md", render_human_gates(ws_data))
    _write("next-actions.md", render_next_actions(ws_data))
    _write("index.html", render_index(ws_data))

    return EvidencePackResult(
        output_dir=output_dir,
        project_name=project_name,
        record_count=len(ws_data.records),
        verdict=ws_data.production_readiness.verdict,
        files=written,
    )
