def _delta_str(delta: int) -> str:
    if delta == 0:
        return "unchanged"
    sign = "+" if delta > 0 else ""
    return f"{sign}{delta}"


def render_recovery_summary(ws: WorkspaceData) -> str:
    """Return recovery-summary.md as a string."""
    p = ws.recovery_progress
    pr = ws.production_readiness

    lines: list[str] = [
        f"# Recovery Summary — {ws.project_name}",
        "",
        f"**Date:** {p.last_date}",
        f"**Period:** {p.first_date} → {p.last_date}",
        f"**Audit records:** {p.record_count}",
        f"**Production verdict:** {pr.verdict}",
        "",
        "read_only: true · applied: false · human validation required",
        "",
        "---",
        "",
        "## Executive Summary",
        "",
        ws.executive_summary,
        "",
        "---",
        "",
        "## Initial State (Baseline)",
        "",
        "| Metric | Value |",
        "|--------|-------|",
    ]
    for key, label in [
        ("critical", "Critical findings"),
        ("important", "Important findings"),
        ("manual", "Manual actions"),
        ("generated", "Generatable SQL blocks"),
    ]:
        lines.append(f"| {label} | {p.baseline.get(key, 0)} |")

    lines += [
        "",
        "## Current State",
        "",
        "| Metric | Baseline | Current | Change |",
        "|--------|----------|---------|--------|",
    ]
    for key, label in [
        ("critical", "Critical findings"),
        ("important", "Important findings"),
        ("manual", "Manual actions"),
        ("generated", "Generatable SQL blocks"),
    ]:
        b = p.baseline.get(key, 0)
        c = p.current.get(key, 0)
        d = p.deltas.get(key, 0)
        lines.append(f"| {label} | {b} | {c} | {_delta_str(d)} |")

    lines += [
        "",
        "---",
        "",
        "## Production Readiness",
        "",
        f"**{pr.verdict}**",
        "",
    ]
    if pr.reasons:
        lines.append("Blocking reasons:")
        lines.append("")
        for r in pr.reasons:
            lines.append(f"- {r}")
    else:
        lines.append("No blocking reasons — all gates passed.")

    lines += ["", "---", "", "_Generated by AEOS Evidence Pack — read-only._"]
    return "\n".join(lines) + "\n"


