@dataclass
class ScaffoldedFile:
    relative_path: str
    written: bool
    skipped: bool = False
    reason: str | None = None


@dataclass
class BuildScaffoldResult:
    project_name: str
    project_type: str
    stack: str
    output_directory: str
    files_created: list[str]
    files_skipped: list[str]
    safety_guarantees: list[str]
    recommended_next_steps: list[str]
    command: str = field(default="build scaffold")
    read_only: bool = field(default=False)
    applied: bool = field(default=True)


def ensure_safe_output_directory(output: Path, force: bool) -> None:
    if output.exists():
        if output.is_file():
            raise ValueError(f"'{output}' is a file, not a directory.")
        if any(output.iterdir()) and not force:
            raise ValueError(
                f"Output directory '{output}' is not empty. Use --force to overwrite."
            )
    else:
        output.mkdir(parents=True)


def _render_readme(name: str, project_type: str, stack: str, plan: BuildPlan) -> str:
    return f"""\
# {name}

> AEOS Build — controlled project from commit zero.

## Project

| Field | Value |
|---|---|
| Name | {name} |
| Type | {project_type} |
| Stack | {stack} |
| Generated by | AEOS Build Scaffold |

## Architecture

{plan.architecture_summary}

## Note

This scaffold is minimal — governance structure only.
No application code has been generated.
All significant changes require human validation.

Run `aeos build plan --name {name} --type {project_type} --stack {stack}`
for the full recommended plan and next steps.
"""


def _render_architecture(name: str, plan: BuildPlan) -> str:
    folder_lines = "\n".join(f"  {f}" for f in plan.folder_structure)
    fence = "```"
    return f"""\
# Architecture — {name}

## Summary

{plan.architecture_summary}

## Recommended Structure

{fence}
{folder_lines}
{fence}

## Decisions to Complete

See `docs/DECISIONS.md` for the decision log.

- [ ] Database choice confirmed and documented
- [ ] Auth provider chosen and documented
- [ ] Deployment target documented before production
- [ ] Exit strategy defined before production deploy
- [ ] External providers identified and assessed
"""


def _render_env_example(name: str, stack: str) -> str:
    base = (
        f"# {name} — Environment Variables\n"
        "# Copy to .env and fill in real values\n"
        "# NEVER commit .env to git — see .gitignore\n"
        "\n"
        "# Application\n"
        f"APP_NAME={name}\n"
        "APP_ENV=development\n"
    )
    return base + _ENV_EXTRAS.get(stack, _ENV_EXTRAS["generic"])


