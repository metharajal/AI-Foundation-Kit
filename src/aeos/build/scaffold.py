def _render_security(name: str, plan: BuildPlan) -> str:
    items = "\n".join(f"- {item}" for item in plan.security_baseline)
    return f"""\
# Security Baseline — {name}

## Core Principles

{items}

## Secrets Management

- `.env` is excluded from git — see `.gitignore`
- `.env.example` contains only placeholders — never real values
- All secrets via environment variables — never hardcoded
- Run `aeos security check --path .` to audit at any time

## Auth Boundaries

> Document auth boundaries here before writing any code.

- [ ] Authentication provider chosen and documented
- [ ] Authorization model defined (RBAC, ABAC, RLS)
- [ ] Session management strategy documented
- [ ] Token expiry and refresh strategy defined
- [ ] API endpoints classified: public vs. protected

## Least Privilege

- [ ] Database user has minimal privileges — no superuser
- [ ] External service accounts scoped to required operations
- [ ] API keys have minimal scope
- [ ] Service-to-service auth documented
"""


def _render_sovereignty(name: str, stack: str, plan: BuildPlan) -> str:
    items = "\n".join(f"- {item}" for item in plan.sovereignty_baseline)
    rows = _PROVIDERS.get(stack, _PROVIDERS["generic"])
    table = "\n".join(f"| {p} | {r} | {s} |" for p, r, s in rows)
    local = _LOCAL_DEV.get(stack, _LOCAL_DEV["generic"])
    return f"""\
# Sovereignty Baseline — {name}

## Core Principles

{items}

## External Dependencies

| Provider | Role | Exit Path |
|---|---|---|
{table}

## Local Development

{local}

## Exit Strategy

> Define your exit strategy before first production deploy.

- [ ] Database migration path documented
- [ ] Auth provider migration path documented
- [ ] Deployment target alternatives identified
- [ ] Data export procedure defined
- [ ] Run `aeos reclaim harden --path . --memory-dir <dir>` to measure

## Portability

Run `aeos sovereignty check --path .` to audit at any time.
"""


def render_scaffold_files(
    name: str,
    project_type: str,
    stack: str,
    plan: BuildPlan,
) -> dict[str, str]:
    today = datetime.date.today().isoformat()
    return {
        "README.md": _render_readme(name, project_type, stack, plan),
        "ARCHITECTURE.md": _render_architecture(name, plan),
        ".env.example": _render_env_example(name, stack),
        ".gitignore": _render_gitignore(),
        "aeos.toml": _render_aeos_toml(name, project_type, stack),
        "docs/DECISIONS.md": _render_decisions(name, today),
        "docs/SECURITY.md": _render_security(name, plan),
        "docs/SOVEREIGNTY.md": _render_sovereignty(name, stack, plan),
    }


def scaffold_build_project(
    name: str,
    project_type: str,
    stack: str,
    output: Path,
    force: bool = False,
) -> BuildScaffoldResult:
    validate_project_type(project_type)
    validate_stack(stack)

    plan = create_build_plan(name, project_type, stack)
    ensure_safe_output_directory(output, force)
    files = render_scaffold_files(name, project_type, stack, plan)

    files_created: list[str] = []
    for rel_path, content in files.items():
        target = output / rel_path
        target.parent.mkdir(parents=True, exist_ok=True)
        target.write_text(content, encoding="utf-8")
        files_created.append(rel_path)

    next_steps = [
        f"Review all generated files in {output}",
        "Fill .env with real values (copy from .env.example — never commit)",
        "git init && git add . && git commit -m 'chore: initial scaffold'",
        f"aeos reclaim harden --path {output} --memory-dir <dir>",
        "Complete ARCHITECTURE.md with a component diagram",
        "Fill docs/DECISIONS.md with first architecture decisions",
    ]

    return BuildScaffoldResult(
        project_name=name,
        project_type=project_type,
        stack=stack,
        output_directory=str(output),
        files_created=files_created,
        files_skipped=[],
        safety_guarantees=list(_SAFETY_GUARANTEES),
        recommended_next_steps=next_steps,
    )


def scaffold_result_to_dict(result: BuildScaffoldResult) -> dict[str, object]:
    return {
        "command": result.command,
        "read_only": result.read_only,
        "applied": result.applied,
        "project_name": result.project_name,
        "project_type": result.project_type,
        "stack": result.stack,
        "output_directory": result.output_directory,
        "files_created": result.files_created,
        "files_skipped": result.files_skipped,
        "safety_guarantees": result.safety_guarantees,
        "recommended_next_steps": result.recommended_next_steps,
    }
